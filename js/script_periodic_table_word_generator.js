function generateImages() {
    const inputText = document.getElementById('inputText').value;
    const outputDiv = document.getElementById('output');
    outputDiv.innerHTML = '';

    let i = 0;
    while (i < inputText.length) {
        let added = false;

        let twoChar = (inputText[i] + inputText[i + 1]).toUpperCase();
        if (periodicTable[twoChar]) {
            // It's a valid two-character symbol, so process it.
            const img = document.createElement('img');
            img.src = periodicTable[twoChar];
            img.alt = twoChar;
            img.className = 'element';
            outputDiv.appendChild(img);
            i += 2; // Skip the next character since we've processed this pair
            added = true;
        }

        // Check if single-letter symbols match
        if (!added && periodicTable[inputText[i].toUpperCase()]) {
            const img = document.createElement('img');
            img.src = periodicTable[inputText[i].toUpperCase()];
            img.alt = inputText[i].toUpperCase();
            img.className = 'element';
            outputDiv.appendChild(img);
            i++; // Increment by one character for single-character match
            added = true;
        }

        // If no match, add the character as a span (for special characters, spaces, etc.)
        if (!added) {
            const span = document.createElement('span');
            span.textContent = inputText[i];
            span.className = 'element';
            outputDiv.appendChild(span);
            i++; // Increment by one character for non-matching character
        }
    }
}
